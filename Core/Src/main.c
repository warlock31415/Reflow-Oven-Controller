float ThermocoupleRead()
{
	char tempBuffer[2] = {0};
	if(HAL_I2C_Master_Transmit(&hi2c1, ThermocoupleADDR, (uint8_t*){Reg_Th}, 1, HAL_MAX_DELAY)==HAL_OK)
		  	  HAL_I2C_Master_Receive(&hi2c1, ThermocoupleADDR, tempBuffer, 2, HAL_MAX_DELAY);
	float temp = 0;
	if (tempBuffer[0] >> 7 == 1)
	{
		temp = (float)((tempBuffer[0]*16+tempBuffer[1]/16)-4096);
    }
	else
	{
	    temp = (float)(tempBuffer[0]*16+(float)tempBuffer[1]/16.0);
	}

	return temp;
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef* htim)
{
	if(htim == &htim1)
	{
		yk = ThermocoupleRead();
		ek = rk - yk;

		uk = 13.21*ek+uk;

		uint16_t D = uk*50/159.0; // Scaling factor -> Convert °C to %

		if (D >= 50)
		{
			uk = 159;
			D = 50;
		}
		if (D <= 0)
		{
			D = 0;
			uk = 0;
		}

		TIM3->CCR1 = D;

		uk2 = uk1;
		uk1 = uk;

		ek2 = ek1;
		ek1 = ek;


		uk = 10.34*ek2 - 23.38*ek1 + 2*uk1 - uk2;




	}
}
